    public function insert($messageToAdd){
        $messageData = $messageToAdd->getAsArray();
        unset($messageData["id"]);
        $query = $this->getQuery_insert($messageData);
        $this->db->query($query);
        $error = $this->db->error();
        if($error["code"] == 0)
            return true;
        return $error;
    }
